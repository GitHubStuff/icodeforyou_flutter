/// The edge along which the [RailMenu] bar is rendered.
enum RailMenuDirection {
  /// Renders the menu bar along the top edge.
  top,

  /// Renders the menu bar along the bottom edge.
  bottom,

  /// Renders the menu bar along the left edge.
  left,

  /// Renders the menu bar along the right edge.
  right,
}
